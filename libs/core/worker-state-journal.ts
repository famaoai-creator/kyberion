// ---------------------------------------------------------------------------
// Generic event-sourcing kernel (op/model contract)
// ---------------------------------------------------------------------------

/** Non-deterministic values carried by the live service, exposed to `apply`. */
export interface OpMeta {
  seq: number;
  ts: string;
}

/**
 * A pure reducer for one op. It MUST return `state` unchanged (same reference)
 * when the op is a no-op, and MUST NOT generate ids/timestamps or perform I/O.
 */
export type OpApply<S, P> = (state: S, payload: P, meta: OpMeta) => S;

export interface ModelDefinition<S> {
  readonly name: string;
  readonly initial: () => S;
}

export interface OpDefinition<S = unknown, P = unknown> {
  readonly name: string;
  readonly model: string;
  readonly schema: z.ZodType<P>;
  readonly apply: OpApply<S, P>;
}

/**
 * Minimal op/model registry + pure projector. Unknown ops and torn journal
 * lines are tolerated (skipped) so a partially-written or forward-versioned
 * record can never poison replay of the rest.
 */
export class EventSourcingKernel {
  private readonly models = new Map<string, ModelDefinition<unknown>>();
  private readonly ops = new Map<string, OpDefinition>();

  defineModel<S>(name: string, initial: () => S): ModelDefinition<S> {
    if (this.models.has(name))
      throw new Error(`[worker-state-journal] model "${name}" already defined`);
    const def: ModelDefinition<S> = { name, initial };
    this.models.set(name, def as ModelDefinition<unknown>);
    return def;
  }

  defineOp<S, P>(
    name: string,
    def: { model: ModelDefinition<S>; schema: z.ZodType<P>; apply: OpApply<S, P> }
  ): OpDefinition<S, P> {
    if (this.ops.has(name)) throw new Error(`[worker-state-journal] op "${name}" already defined`);
    if (!this.models.has(def.model.name)) {
      throw new Error(
        `[worker-state-journal] op "${name}" references unknown model "${def.model.name}"`
      );
    }
    const op: OpDefinition<S, P> = {
      name,
      model: def.model.name,
      schema: def.schema,
      apply: def.apply,
    };
    this.ops.set(name, op as OpDefinition);
    return op;
  }

  hasOp(name: string): boolean {
    return this.ops.has(name);
  }

  /** Validate a payload against its op schema (throws on an invalid contract). */
  validatePayload(opName: string, payload: unknown): unknown {
    const op = this.ops.get(opName);
    if (!op) throw new Error(`[worker-state-journal] unknown op "${opName}"`);
    return op.schema.parse(payload);
  }

  /**
   * Fold a stream of validated + migrated envelopes into a per-model state map.
   * Pure: no I/O, no clock, no emission. Callers wrap this in
   * {@link runInRestoreMode} during restore to structurally forbid side effects.
   */
  project(events: readonly JournalEventEnvelope[]): Map<string, unknown> {
    const states = new Map<string, unknown>();
    for (const [name, model] of this.models) states.set(name, model.initial());
    for (const event of events) {
      const op = this.ops.get(event.op);
      if (!op) continue; // unknown/forward op — tolerate
      let payload: unknown;
      try {
        payload = op.schema.parse(event.payload);
      } catch {
        continue; // a payload that no longer validates must not poison replay
      }
      const current = states.get(op.model);
      const next = op.apply(current, payload, { seq: event.seq, ts: event.ts });
      states.set(op.model, next);
    }
    return states;
  }
}

// ---------------------------------------------------------------------------
// Restore mode: structural "no side effects during replay" guard
// ---------------------------------------------------------------------------

let RESTORING = false;

/** True while a `restore()` (validate → migrate → replay → hooks) is running. */
export function isRestoring(): boolean {
  return RESTORING;
}

/**
 * Guard placed at every emission seam (notification / LLM / tool). Throws when
 * called during restore — this is how "no UI/LLM/tool during restore" is
 * enforced structurally rather than by convention.
 */
export function assertNotDuringRestore(seam: string): void {
  if (RESTORING) {
    throw new Error(
      `[worker-state-journal] "${seam}" is forbidden during restore: replay must not emit notifications, call the model, or execute tools`
    );
  }
}

/** Run `fn` with the restore guard armed; restores the prior flag afterwards. */
export function runInRestoreMode<T>(fn: () => T): T {
  const prev = RESTORING;
  RESTORING = true;
  try {
    return fn();
  } finally {
    RESTORING = prev;
  }
}

/**
 * THE one-shot-reminder emission seam. The live service calls this to actually
 * hand a fork/cancel reminder to the model, then appends a `reminder.consume`
 * op so the log records that it was delivered. Calling it during restore throws
 * (see {@link assertNotDuringRestore}), guaranteeing replay never re-delivers.
 */
export function deliverOneShotReminder(
  reminder: OneShotReminder,
  sink: (reminder: OneShotReminder) => void
): void {
  assertNotDuringRestore('deliverOneShotReminder');
  sink(reminder);
}

// ---------------------------------------------------------------------------
// Journal envelope + versioned migration
// ---------------------------------------------------------------------------

/** Current on-disk journal schema version. New records are written at this version. */
export const CURRENT_JOURNAL_VERSION = 2;

export const journalEventEnvelopeSchema = z
  .object({
    /** Schema version of THIS record (migrated up to {@link CURRENT_JOURNAL_VERSION}). */
    v: z.number().int().positive(),
    seq: z.number().int().nonnegative(),
    ts: z.string(),
    op: z.string(),
    payload: z.unknown(),
  })
  .strict();

export type JournalEventEnvelope = z.infer<typeof journalEventEnvelopeSchema>;

/** A migration transforms a record from version `v` to version `v + 1`. */
export type JournalMigration = (env: JournalEventEnvelope) => JournalEventEnvelope;

/**
 * Version-keyed migration chain. Keyed by the *source* version.
 *
 * v1→v2: early journals recorded a delegation's lifecycle under `state`; v2
 * renamed it to `status` (aligning with the KC-06 delegation record). The
 * migration renames the field silently, proving the migration chain works and
 * that a v1 journal remains readable alongside v2 records.
 */
export const JOURNAL_MIGRATIONS: Record<number, JournalMigration> = {
  1: (env) => {
    if (env.op === 'delegation.upsert' && env.payload && typeof env.payload === 'object') {
      const payload = env.payload as Record<string, unknown>;
      if ('state' in payload && !('status' in payload)) {
        const { state, ...rest } = payload;
        return { ...env, v: 2, payload: { ...rest, status: state } };
      }
    }
    return { ...env, v: 2 };
  },
};

/**
 * Migrate a single envelope up to {@link CURRENT_JOURNAL_VERSION}. Throws when a
 * step is missing (unknown intermediate version) or when the record is *newer*
 * than this code — a journal from the future must not be silently misread.
 */
export function migrateEnvelope(env: JournalEventEnvelope): JournalEventEnvelope {
  let current = env;
  if (current.v > CURRENT_JOURNAL_VERSION) {
    throw new Error(
      `[worker-state-journal] journal record v${current.v} is newer than supported v${CURRENT_JOURNAL_VERSION}`
    );
  }
  while (current.v < CURRENT_JOURNAL_VERSION) {
    const migrate = JOURNAL_MIGRATIONS[current.v];
    if (!migrate) throw new Error(`[worker-state-journal] no migration from journal v${current.v}`);
    const next = migrate(current);
    if (next.v <= current.v) {
      throw new Error(
        `[worker-state-journal] migration from v${current.v} did not advance the version`
      );
    }
    current = next;
  }
  return current;
}

