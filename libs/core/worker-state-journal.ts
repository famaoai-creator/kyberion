/**
 * Event-sourced worker state restore contract (KD-03).
 *
 * Promotes the KC-02 `worker-event-stream.ts` observation stream (record/replay)
 * into **state reconstruction**. The KC-02 stream answers "what happened"; this
 * module answers "what state am I in after a process restart", reconstructed
 * *purely* from an append-only journal. It is the worker-granularity precedent
 * for MO-06 (durable resume of the in-memory `MissionCoordinationBus`); that bus
 * stays untouched here.
 *
 * Modeled on kimi-code `wire/` (op/model + restore + versioned migration), with
 * Kyberion vocabulary and invariants:
 *
 *  1. **op/model contract** — restorable state (KD-01 goal, delegation ledger /
 *     active background tasks, pending-approval references) is declared via
 *     {@link EventSourcingKernel.defineModel} + {@link
 *     EventSourcingKernel.defineOp}. Every `apply` is a **pure** function that
 *     returns the *same reference* when nothing changes. Non-deterministic
 *     values (ids, timestamps) are generated by the live service and carried in
 *     the op payload — never invented inside `apply`.
 *  2. **restore contract** — validate → versioned migration ({@link
 *     CURRENT_JOURNAL_VERSION}, with a v1→v2 migration in {@link
 *     JOURNAL_MIGRATIONS}) → **silent** replay → ordered `onDidRestore` hooks.
 *     Restore runs inside {@link runInRestoreMode}; the emission seam {@link
 *     deliverOneShotReminder} throws if invoked while restoring, so no
 *     UI/notification, LLM call, or tool execution can happen during replay —
 *     enforced structurally, not by convention.
 *  3. **CQRS separation** — the JSONL journal (secure-io) is authoritative. The
 *     derived goal/delegation summary index ({@link
 *     WorkerStateJournal.summary}) is rebuildable: on corruption it is wiped and
 *     reprojected from the journal.
 *  4. **fork/clear hygiene** — a mission fork or goal cancel records exactly one
 *     one-shot reminder (KD-01's {@link GOAL_CANCEL_SYSTEM_REMINDER} / a fork
 *     variant) telling the model to ignore prior reminders. Replay reconstructs
 *     the *pending* reminder deterministically but never re-delivers it —
 *     delivery only ever happens via a live `reminder.consume` append.
 *  5. **KD-01 integration** — after restore, an `active` goal comes back
 *     `paused` ({@link demoteActiveOnResume}); the restored goal feeds
 *     `runGoalDrivenLoop({ resumeFrom })` unchanged.
 */

