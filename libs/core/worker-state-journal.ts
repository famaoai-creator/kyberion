export interface DelegationLedgerEntry {
  delegationId: string;
  owner: string;
  instruction: string;
  status: 'started' | 'completed' | 'failed';
  /** KC-06 background/async delegation — an "active background task" while `started`. */
  background?: boolean;
  createdAt: string;
  completedAt?: string;
  resultSummary?: string;
  error?: string;
  missionId?: string;
}

/** Pending approvals are held by *reference* only — never the approval payload. */
export interface PendingApprovalRef {
  requestId: string;
  channel?: string;
  requestedBy?: string;
}

export type OneShotReminderOrigin = 'goal_cancel' | 'mission_fork';

export interface OneShotReminder {
  id: string;
  text: string;
  origin: OneShotReminderOrigin;
}

/** The full worker state reconstructable from the journal. */
export interface WorkerRestorableState {
  goal: GoalRuntimeState | null;
  delegations: Record<string, DelegationLedgerEntry>;
  pendingApprovals: Record<string, PendingApprovalRef>;
  /** At most one undelivered fork/cancel reminder (see fork/clear hygiene). */
  pendingReminder: OneShotReminder | null;
}

function initialWorkerState(): WorkerRestorableState {
  return { goal: null, delegations: {}, pendingApprovals: {}, pendingReminder: null };
}

const goalRuntimeStateSchema = z
  .object({
    goalId: z.string(),
    objective: z.string(),
    state: z.enum(GOAL_STATES),
    turnCount: z.number(),
    blockedStreak: z.number(),
    pendingBlockerSignature: z.string().optional(),
    terminalKind: z.enum(['technical', 'business']).optional(),
    terminalReason: z.string().optional(),
    missionId: z.string().optional(),
    createdAt: z.string(),
    updatedAt: z.string(),
    budget: z
      .object({
        tokenBudget: z.number().optional(),
        turnBudget: z.number().optional(),
        wallClockBudgetMs: z.number().optional(),
      })
      .optional(),
    budgetStats: z
      .object({
        tokensUsed: z.number(),
        turnsUsed: z.number(),
        wallClockMsUsed: z.number(),
      })
      .optional(),
  })
  .strict();

const delegationLedgerEntrySchema = z
  .object({
    delegationId: z.string(),
    owner: z.string(),
    instruction: z.string(),
    status: z.enum(['started', 'completed', 'failed']),
    background: z.boolean().optional(),
    createdAt: z.string(),
    completedAt: z.string().optional(),
    resultSummary: z.string().optional(),
    error: z.string().optional(),
    missionId: z.string().optional(),
  })
  .strict();

const pendingApprovalRefSchema = z
  .object({
    requestId: z.string(),
    channel: z.string().optional(),
    requestedBy: z.string().optional(),
  })
  .strict();

/** The one-shot fork reminder, mirroring KD-01's {@link GOAL_CANCEL_SYSTEM_REMINDER}. */
export const MISSION_FORK_SYSTEM_REMINDER =
  'This worker was forked to a new mission. Ignore all earlier active-goal and continuation ' +
  'reminders inherited from the parent; only goals stated for THIS mission apply.';

/** Shared kernel holding the worker model + ops. */
export const workerStateKernel = new EventSourcingKernel();

const workerModel = workerStateKernel.defineModel<WorkerRestorableState>(
  'worker',
  initialWorkerState
);

/** op names as constants so callers/tests never guess the strings. */
export const WORKER_STATE_OPS = {
  goalUpsert: 'goal.upsert',
  goalCancel: 'goal.cancel',
  delegationUpsert: 'delegation.upsert',
  approvalRequest: 'approval.request',
  approvalResolve: 'approval.resolve',
  missionFork: 'mission.fork',
  reminderConsume: 'reminder.consume',
} as const;

workerStateKernel.defineOp(WORKER_STATE_OPS.goalUpsert, {
  model: workerModel,
  schema: goalRuntimeStateSchema,
  apply: (state, goal) => ({ ...state, goal }),
});

workerStateKernel.defineOp(WORKER_STATE_OPS.goalCancel, {
  model: workerModel,
  schema: z.object({ goalId: z.string(), reminderId: z.string() }).strict(),
  apply: (state, payload) => ({
    ...state,
    goal: null,
    pendingReminder: {
      id: payload.reminderId,
      text: GOAL_CANCEL_SYSTEM_REMINDER,
      origin: 'goal_cancel' as const,
    },
  }),
});

workerStateKernel.defineOp(WORKER_STATE_OPS.delegationUpsert, {
  model: workerModel,
  schema: delegationLedgerEntrySchema,
  apply: (state, entry) => ({
    ...state,
    delegations: { ...state.delegations, [entry.delegationId]: entry },
  }),
});

workerStateKernel.defineOp(WORKER_STATE_OPS.approvalRequest, {
  model: workerModel,
  schema: pendingApprovalRefSchema,
  apply: (state, ref) => ({
    ...state,
    pendingApprovals: { ...state.pendingApprovals, [ref.requestId]: ref },
  }),
});

workerStateKernel.defineOp(WORKER_STATE_OPS.approvalResolve, {
  model: workerModel,
  schema: z.object({ requestId: z.string() }).strict(),
  apply: (state, payload) => {
    if (!(payload.requestId in state.pendingApprovals)) return state; // no-op => same reference
    const next = { ...state.pendingApprovals };
    delete next[payload.requestId];
    return { ...state, pendingApprovals: next };
  },
});

workerStateKernel.defineOp(WORKER_STATE_OPS.missionFork, {
  model: workerModel,
  schema: z.object({ reminderId: z.string(), forkedFrom: z.string().optional() }).strict(),
  apply: (state, payload) => ({
    ...state,
    pendingReminder: {
      id: payload.reminderId,
      text: MISSION_FORK_SYSTEM_REMINDER,
      origin: 'mission_fork' as const,
    },
  }),
});

workerStateKernel.defineOp(WORKER_STATE_OPS.reminderConsume, {
  model: workerModel,
  schema: z.object({ reminderId: z.string() }).strict(),
  apply: (state, payload) => {
    // Idempotent: consuming a reminder that isn't pending is a no-op and MUST
    // return the same reference (proves replay never re-fires a consumed one).
    if (!state.pendingReminder || state.pendingReminder.id !== payload.reminderId) return state;
    return { ...state, pendingReminder: null };
  },
});

// ---------------------------------------------------------------------------
// Derived index (CQRS): goal/delegation summary lookup
// ---------------------------------------------------------------------------

export interface GoalDelegationSummary {
  goalId: string | null;
  goalState: GoalState | null;
  delegationCount: number;
  /** Delegations still `started` — the "active background tasks" count. */
  activeDelegationCount: number;
  pendingApprovalCount: number;
  hasPendingReminder: boolean;
  /** Journal seq this summary was projected from (staleness check aid). */
  projectedThroughSeq: number;
}

const goalDelegationSummarySchema = z
  .object({
    goalId: z.string().nullable(),
    goalState: z.enum(GOAL_STATES).nullable(),
    delegationCount: z.number().int().nonnegative(),
    activeDelegationCount: z.number().int().nonnegative(),
    pendingApprovalCount: z.number().int().nonnegative(),
    hasPendingReminder: z.boolean(),
    projectedThroughSeq: z.number().int(),
  })
  .strict();

function summarize(
  state: WorkerRestorableState,
  projectedThroughSeq: number
): GoalDelegationSummary {
  const delegations = Object.values(state.delegations);
  return {
    goalId: state.goal?.goalId ?? null,
    goalState: state.goal?.state ?? null,
    delegationCount: delegations.length,
    activeDelegationCount: delegations.filter((d) => d.status === 'started').length,
    pendingApprovalCount: Object.keys(state.pendingApprovals).length,
    hasPendingReminder: state.pendingReminder !== null,
    projectedThroughSeq,
  };
}

// ---------------------------------------------------------------------------
// WorkerStateJournal: authoritative append log + restore + self-healing index
// ---------------------------------------------------------------------------

export interface WorkerStateJournalOptions {
  /** Authoritative append-only JSONL journal (resolved through secure-io). */
  journalPath: string;
  /** Derived summary index; defaults to `<journalPath>.index.json`. */
  indexPath?: string;
  now?: () => string;
}

export interface OnDidRestoreHook {
  (state: WorkerRestorableState): void;
}

interface ReadResult {
  events: JournalEventEnvelope[];
  maxSeq: number;
}

export class WorkerStateJournal {
  private readonly kernel = workerStateKernel;
  private readonly journalPath: string;
  private readonly indexPath: string;
  private readonly now: () => string;
  private readonly restoreHooks: Array<{ order: number; index: number; hook: OnDidRestoreHook }> =
    [];
  private hookCounter = 0;
  private seqLoaded = false;
  private seq = 0;

  constructor(options: WorkerStateJournalOptions) {
    this.journalPath = pathResolver.rootResolve(options.journalPath);
    this.indexPath = options.indexPath
      ? pathResolver.rootResolve(options.indexPath)
      : `${this.journalPath}.index.json`;
    this.now = options.now ?? (() => new Date().toISOString());
  }

  /** Register an ordered post-restore hook (lower `order` runs first). */
  onDidRestore(hook: OnDidRestoreHook, order = 0): () => void {
    const entry = { order, index: this.hookCounter++, hook };
    this.restoreHooks.push(entry);
    return () => {
      const at = this.restoreHooks.indexOf(entry);
      if (at >= 0) this.restoreHooks.splice(at, 1);
    };
  }

  // -- live append (side effects allowed; not during restore) ----------------

  /**
   * Validate a payload against its op contract, assign seq/ts from the live
   * service, and append the record to the authoritative journal. Returns the
   * written envelope. Appending is the authoritative act only — reminder
   * *delivery* is a separate, guarded step ({@link deliverOneShotReminder}).
   */
  append(opName: string, payload: unknown): JournalEventEnvelope {
    assertNotDuringRestore('WorkerStateJournal.append');
    const validated = this.kernel.validatePayload(opName, payload);
    this.ensureSeqLoaded();
    const envelope: JournalEventEnvelope = journalEventEnvelopeSchema.parse({
      v: CURRENT_JOURNAL_VERSION,
      seq: this.seq++,
      ts: this.now(),
      op: opName,
      payload: validated,
    });
    this.ensureDir(this.journalPath);
    safeAppendFileSync(this.journalPath, `${JSON.stringify(envelope)}\n`);
    return envelope;
  }

  recordGoal(goal: GoalRuntimeState): JournalEventEnvelope {
    return this.append(WORKER_STATE_OPS.goalUpsert, goal);
  }

  cancelGoal(goalId: string, reminderId: string): JournalEventEnvelope {
    return this.append(WORKER_STATE_OPS.goalCancel, { goalId, reminderId });
  }

  recordDelegation(entry: DelegationLedgerEntry): JournalEventEnvelope {
    return this.append(WORKER_STATE_OPS.delegationUpsert, entry);
  }

  requestApproval(ref: PendingApprovalRef): JournalEventEnvelope {
    return this.append(WORKER_STATE_OPS.approvalRequest, ref);
  }

  resolveApproval(requestId: string): JournalEventEnvelope {
    return this.append(WORKER_STATE_OPS.approvalResolve, { requestId });
  }

  forkMission(reminderId: string, forkedFrom?: string): JournalEventEnvelope {
    return this.append(WORKER_STATE_OPS.missionFork, {
      reminderId,
      ...(forkedFrom ? { forkedFrom } : {}),
    });
  }

  /** Record that the current pending reminder was delivered (so replay won't re-fire it). */
  consumeReminder(reminderId: string): JournalEventEnvelope {
    return this.append(WORKER_STATE_OPS.reminderConsume, { reminderId });
  }

  // -- restore (silent, pure) ------------------------------------------------

  /**
   * Reconstruct the worker state purely from the journal:
   * validate → migrate → silent replay → demote active goal → ordered hooks.
   *
   * The whole body runs inside {@link runInRestoreMode}, so any emission seam
   * (notification / LLM / tool) invoked by a hook throws instead of firing.
   * After restore, `this.seq` continues from the journal's max seq, so a
   * live append picks up exactly where the dead process left off.
   */
  restore(): WorkerRestorableState {
    const read = this.readJournal();
    this.seq = read.maxSeq + 1;
    this.seqLoaded = true;
    return runInRestoreMode(() => {
      const states = this.kernel.project(read.events);
      let state = (states.get('worker') as WorkerRestorableState) ?? initialWorkerState();
      // KD-01: a persisted `active` goal means the prior process died mid-turn —
      // demote to `paused` so it never self-advances until an explicit resume.
      if (state.goal && state.goal.state === 'active') {
        state = { ...state, goal: demoteActiveOnResume(state.goal, this.now) };
      }
      const ordered = [...this.restoreHooks].sort((a, b) => a.order - b.order || a.index - b.index);
      for (const { hook } of ordered) {
        hook(state);
      }
      return state;
    });
  }

  // -- derived index (CQRS, self-healing) ------------------------------------

  /**
   * Return the goal/delegation summary. Reads the derived index file; if it is
   * missing or corrupt, wipes it and reprojects from the authoritative journal
   * (self-healing) before returning.
   */
  summary(): GoalDelegationSummary {
    const loaded = this.loadIndex();
    if (loaded) return loaded;
    return this.rebuildSummary();
  }

  /** Force a reprojection of the derived index from the journal. */
  rebuildSummary(): GoalDelegationSummary {
    const read = this.readJournal();
    const state = runInRestoreMode(() => {
      const states = this.kernel.project(read.events);
      return (states.get('worker') as WorkerRestorableState) ?? initialWorkerState();
    });
    const summary = summarize(state, read.maxSeq);
    try {
      this.ensureDir(this.indexPath);
      safeWriteFile(this.indexPath, `${JSON.stringify(summary, null, 2)}\n`);
    } catch (error) {
      // The index is derived and rebuildable; never fail on a write hiccup.
      logger.warn(
        `[worker-state-journal] derived index write failed (non-fatal): ${
          error instanceof Error ? error.message : String(error)
        }`
      );
    }
    return summary;
  }

  private loadIndex(): GoalDelegationSummary | null {
    if (!safeExistsSync(this.indexPath)) return null;
    try {
      const raw = String(safeReadFile(this.indexPath, { encoding: 'utf-8' }));
      // Reconstruct explicitly: zod v4 infers `.nullable()` keys as optional,
      // so round-trip through the hand-written contract shape instead of the
      // schema's looser inferred type.
      const parsed = goalDelegationSummarySchema.parse(JSON.parse(raw));
      return {
        goalId: parsed.goalId ?? null,
        goalState: parsed.goalState ?? null,
        delegationCount: parsed.delegationCount,
        activeDelegationCount: parsed.activeDelegationCount,
        pendingApprovalCount: parsed.pendingApprovalCount,
        hasPendingReminder: parsed.hasPendingReminder,
        projectedThroughSeq: parsed.projectedThroughSeq,
      };
    } catch {
      // Corrupt derived index: wipe so the next reader reprojects cleanly.
      try {
        safeRmSync(this.indexPath, { force: true });
      } catch {
        /* best-effort */
      }
      return null;
    }
  }

  // -- internals -------------------------------------------------------------

  private ensureSeqLoaded(): void {
    if (this.seqLoaded) return;
    this.seq = this.readJournal().maxSeq + 1;
    this.seqLoaded = true;
  }

  private readJournal(): ReadResult {
    if (!safeExistsSync(this.journalPath)) return { events: [], maxSeq: -1 };
    const raw = String(safeReadFile(this.journalPath, { encoding: 'utf-8' }));
    const events: JournalEventEnvelope[] = [];
    let maxSeq = -1;
    for (const line of raw.split('\n')) {
      const trimmed = line.trim();
      if (!trimmed) continue;
      try {
        const parsed = journalEventEnvelopeSchema.parse(JSON.parse(trimmed));
        const migrated = migrateEnvelope(parsed);
        events.push(migrated);
        if (migrated.seq > maxSeq) maxSeq = migrated.seq;
      } catch {
        // A torn/corrupt/forward line must not poison replay of the rest.
      }
    }
    return { events, maxSeq };
  }

  private ensureDir(filePath: string): void {
    const dir = filePath.replace(/[/\\][^/\\]+$/, '');
    if (dir && dir !== filePath && !safeExistsSync(dir)) {
      safeMkdir(dir, { recursive: true });
    }
  }
}
